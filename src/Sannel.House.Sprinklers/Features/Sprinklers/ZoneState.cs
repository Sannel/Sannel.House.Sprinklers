namespace Sannel.House.Sprinklers.Features.Sprinklers;

/// <summary>
/// Enum representing the state of a sprinkler zone.
/// </summary>
public enum ZoneState : byte
{
	/// <summary>
	/// Sprinkler zone is off.
	/// </summary>
	Off = 0,

	/// <summary>
	/// Sprinkler zone is on.
	/// </summary>
	On = 1
}
